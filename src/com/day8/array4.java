package com.day8;

public class array4 {
	
	public static void main(String[] args) {
		
		//create double array and print.
		
		double[] arr = new double[4];
		arr[0] = 10.5;
		arr[1] = 20.2;
		arr[2] = 30.8;
		arr[3] = 40.1;

		for (double value : arr) {
		    System.out.println(value);
		}
	}

}

