package com.day5;

public class switch13 {
	
	public static void main(String[] args) {
		
//switch case can work with char.
		
		char c1='c';
		
		switch (c1) { 
		
		case '1':
			System.out.println("hello");
			break;
		case 'f':
			System.out.println("byee");
			break;

		default: 
			System.out.println("default"); //default
			break;
		}

}
}
