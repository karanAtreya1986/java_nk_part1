package com.day5;

public class switch9 {
	
	public static void main(String[] args) {
		
//		switch case works with byte.
		
		byte marks=10;
		
		switch (marks) {
		case 1:
			System.out.println("hello");
			break;
		case 2:
			System.out.println("byee");
			break;

		default: 
			System.out.println("default"); //default
			break;
		}

}
}
