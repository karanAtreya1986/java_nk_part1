package com.day7;

public class array16 {
	
	public static void main(String[] args) {
		
		//characters ascii value will be printed.
		
		int[] arr = new int[4];
		arr[0] = 10;
		arr[1] = 'a';
		arr[2] = 'R' ;
		arr[3] = 40;
		
		System.out.println(arr[1]);//97
		System.out.println(arr[0]);//10
		System.out.println(arr[2]);//82
		System.out.println(arr[3]);//40
		
	}

}






