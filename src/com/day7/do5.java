package com.day7;

public class do5 {
	
	public static void main(String[] args) {
		
		//increment decrement has to come inside do not while.
		//else goes into infinite looping.
		
		int i=1;
		do {
			System.out.println(i); 
			
		}
		while(i<=6);
		i++;
		
		
	}
	
	
	

}

//prints 1 infinite times.


