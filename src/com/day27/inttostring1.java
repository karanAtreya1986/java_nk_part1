package com.day27;

public class inttostring1 {
	
	public static void main(String[] args) {
		
		//convert any number to string using valueof.
		
		int t=100;
		System.out.println(t+20); //120
		String valueOf = String.valueOf(t);
		System.out.println(valueOf); //100
		System.out.println(valueOf+20); //10020
	}

}



