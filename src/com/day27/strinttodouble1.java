package com.day27;

public class strinttodouble1 {
	
	public static void main(String[] args) {
		
		//string to double.
		
		String total="100.23";
		System.out.println(total+20); //100.2320
		double double1 = Double.parseDouble(total);
		System.out.println(double1); //100.23
		System.out.println(double1+20);//120.23
	
	}

}





