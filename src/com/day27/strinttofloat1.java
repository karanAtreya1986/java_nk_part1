package com.day27;

public class strinttofloat1 {
	
	//string to float.
	public static void main(String[] args) {
		
		//string to double.
		
		String total="100.23";
		System.out.println(total+20); //100.2320
		float double1 = Float.parseFloat(total);
		System.out.println(double1); //100.23
		System.out.println(double1+20);//120.23
	
	}

}




