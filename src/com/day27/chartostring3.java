package com.day27;

public class chartostring3 {
	
	public static void main(String[] args) {
		
		//convert any number/character to string using valueof.
		
		char t='a';
		System.out.println(t+20); //117
		String valueOf = String.valueOf(t);
		System.out.println(valueOf); //a
		System.out.println(valueOf+20); //a20
	}

}






