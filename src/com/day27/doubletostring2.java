package com.day27;

public class doubletostring2 {
	
	public static void main(String[] args) {
		
		//convert any number to string using valueof.
		
		double t=125.89;
		System.out.println(t+20); //145.89
		String valueOf = String.valueOf(t);
		System.out.println(valueOf); //125.89
		System.out.println(valueOf+20); //125.8920
	}

}



