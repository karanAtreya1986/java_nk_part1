package com.day4;

public class incredecre8 {
	
	public static void main(String[] args) {
		
		//First print the total value, then increment in memory.
		
		int total=10;
		
		System.out.println(total++); //10
		System.out.println(total--); //11
		System.out.println(++total); //11
		System.out.println(--total); //10
	}

}
