package com.day4;

public class incredecre25 {
	
	public static void main(String[] args) {
		
		//Get the char from any number.

		int a=56;
		System.out.println((char)a); //8
		
		float b=45.78f;
		System.out.println((char)b); //-
		
		double c=23434.32434;
		System.out.println((char)c); //宊
		
		char d='r';
		System.out.println((char)d); //r
		
		byte e=90;
		System.out.println((char)e); //Z
		
		long f= 423424L;
		System.out.println((char)f); //瘀
		
		short g=456;
		System.out.println((char)g); //ǈ
		
//		boolean b1=true;
//		System.out.println((char)b1); //Cannot cast from boolean to char
		
		
	}

}
