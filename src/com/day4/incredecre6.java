package com.day4;

public class incredecre6 {
	
	public static void main(String[] args) {
		
		//First print the total value, then increment in memory.
		
		int total=10;
		int total1=10;
		int total2=10;
		int total3=10;
		
		System.out.println(total++); //10
		System.out.println(total1--); //10
		System.out.println(++total2); //11
		System.out.println(--total3); //9
	}

}
